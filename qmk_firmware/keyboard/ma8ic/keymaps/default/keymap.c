bool process_record_user(uint16_t keycode, keyrecord_t *record)
{
    if(layer_state_is(_FUNC)){
        switch (keycode)
        {
            default:
            break;
        }
    }

    switch (keycode)
    {
        /* KEYBOARD PET STATUS START */
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        /* KEYBOARD PET STATUS END */
        default:
        break;
    }
    anim_sleep = timer_read32();
    
    return true;
}

